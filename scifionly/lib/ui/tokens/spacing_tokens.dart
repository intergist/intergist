/// Spacing scale for the SciFiOnly design system.
///
/// Based on a 4 px base unit. Use these tokens for all padding, margin,
/// and gap values to keep the UI consistent.
abstract final class AppSpace {
  /// 2 px — hairline insets, icon padding.
  static const double xxs = 2;

  /// 4 px — tight insets, compact chip padding.
  static const double xs = 4;

  /// 8 px — default inner padding.
  static const double sm = 8;

  /// 12 px — standard gap between related elements.
  static const double md = 12;

  /// 16 px — section padding, card insets.
  static const double lg = 16;

  /// 20 px — generous inner section spacing.
  static const double xl = 20;

  /// 24 px — major section breaks.
  static const double xxl = 24;

  /// 32 px — large panel padding, page margins.
  static const double xxxl = 32;

  /// 40 px — hero spacing.
  static const double huge = 40;

  /// 48 px — extra-large page gutters.
  static const double massive = 48;

  /// 64 px — full-section dividers.
  static const double giant = 64;
}
